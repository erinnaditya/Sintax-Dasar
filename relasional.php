	<?php
		$a = 5;
		$b = 10;
		echo "\$a == \$b";
		echo "<br/>";
		echo "<br/> \$a == \$b : " . ($a == $b);
		echo "<br/> \$a != \$b : " . ($a != $b);
		echo "<br/> \$a < \$b : " . ($a < $b);
		echo "<br/> \$a > \$b : " . ($a > $b);
		echo "<br/> \$a <= \$b : " . ($a <= $b);
		echo "<br/> \$a >= \$b : " . ($a >= $b);
	?>
	
